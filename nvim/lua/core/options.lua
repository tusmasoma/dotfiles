-- core/options.lua
vim.opt.number = true             -- 行番号
vim.opt.relativenumber = true     -- 相対行番号
vim.opt.tabstop = 4               -- タブを4スペース
vim.opt.shiftwidth = 4            -- インデント幅
vim.opt.expandtab = true          -- タブをスペースに変換
vim.opt.clipboard = "unnamedplus"  -- クリップボード共有
vim.opt.ignorecase = true         -- 大文字小文字を無視した検索
vim.opt.smartcase = true          -- 検索時に大文字小文字を含む場合は区別
